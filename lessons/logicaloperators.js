// Logical Operations "AND"

console.log(false && true) // all values have to be TRUE for expression to be true

// EXAMPLE of && "AND" Operator in use
var ageIsMoreThanEighteen = true
var isUSCitizen = false

var eligibilityForDriversLicence = ageIsMoreThanEighteen && isUSCitizen
console.log('This customer is eligible for DL: ' + eligibilityForDriversLicence)



// Logical Operator "OR" ||
console.log(true || false) // any value should be TRUE for the expression to be True

// EXAMPLE of || Operator in use
var ageIsMoreThanEighteen = true
var isUSCitizen = false

var eligibilityForDriversLicence = ageIsMoreThanEighteen || isUSCitizen
console.log('This customer is eligible for DL: ' + eligibilityForDriversLicence)


// Logical "NOT" ! Operator
console.log(!true)
console.log(6 >= 10)

