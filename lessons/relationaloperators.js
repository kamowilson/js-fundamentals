// Relational or comparison operators

// > = more than
// < = less than
// >= more than equal
// <= less than equal

var result = 1 >= 5
console.log(result)

// Equality operators

var x = 1
console.log(x == "1") //loose comparison meaning we just compare the value and they are the same
console.log(x === "1") //strict comparison means we are not only checking the value but we are checking the data type also
